<h1>Cuke4PHP</h1>

<p>
Cuke4php is a PHP implementation of a Cucumber wire protocol server.  
This allows step definitions to be written in PHP, which facilitates direct interaction with PHP Code.
</p>

<h2>Best Practices</h2>
<h3>When to use</h3>
<ul>
    <li>For complex business logic</li>
    <li>For describing the behavior of legacy code</li>
    <li>For high level functional testing</li>
</ul>

<h3>When <u>not</u> to use</h3>
<ul>
    <li>As a unit test framework</li>
</ul>
<p>
    Cucumber is not intended to be a replacement for simple unit tests.  In general, tests written using PHPUnit or other frameworks will be faster to execute.
</p>

